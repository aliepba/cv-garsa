                    ->join('pegawai', 'schedule.no_pegawai', '=', 'pegawai.no_pegawai')
                    ->join('upah' , 'schedule.upah_id', '=', 'upah.id')
                    ->select('schedule.*', 'pegawai.nama', 'upah.nama_barang')
                    ->get();

            // dd($data);

        return view('pages.hr.schedule.index', [
            'data' => $data
